
public class S05Athlete {
	private String name_;
	private String category_;
	private int allTimeRanking_;
	public String getName() {
		return name_;
	}
	public void setName(String name_) {
		this.name_ = name_;
	}
	public String getCategory() {
		return category_;
	}
	public void setCategory(String category_) {
		this.category_ = category_;
	}
	public int getAllTimeRanking() {
		return allTimeRanking_;
	}
	public void setAllTimeRanking(int allTimeRanking_) {
		this.allTimeRanking_ = allTimeRanking_;
	}
}
